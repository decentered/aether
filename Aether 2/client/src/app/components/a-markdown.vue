<template>
  <div class="markdowned" v-html="compiledMarkdown"></div>
</template>

<script lang="ts">
  var MDRenderer = require('../../app/services/markdown.ts')
  export default {
    name: 'a-markdown',
    props: ['content'],
    data() {
      return {}
    },
    computed: {
      compiledMarkdown(this: any) {
        if (typeof this.content !== 'undefined') {
          return MDRenderer(this.content)
        }
        return ""
      },
    }
  }
</script>

